package com.xworkz.array.custom_array_example;

public class Perfume extends Object {
	String name;
	String brand;
	
	public Perfume(String name, String brand) {
		
		this.name = name;
		this.brand = brand;
	}

	@Override
	public String toString() {
		return "Perfume [name=" + name + ", brand=" + brand + "]";
	}
	
	

}
