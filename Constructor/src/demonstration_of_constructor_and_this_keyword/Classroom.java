package demonstration_of_constructor_and_this_keyword;

public class Classroom {
	int noOfStudents;
	String background;
	String subjects;
	
void Classroom()
{
	
}

void Classroom(int noOfStudents,String background)
{
	noOfStudents=noOfStudents;
	background=background;
	
}

void Classroom(String subjects)
{
	subjects= subjects;
}

void Classroom(int noOfStudents,String background,String subjects)
{
	noOfStudents=noOfStudents;
	background=background;
	subjects= subjects;
	
}

}
